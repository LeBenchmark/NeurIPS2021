#!/bin/bash

if [[ $# -ne 1 ]]; then
	echo "Usage: $0 <.scored file>"
	echo "          - .scored file is generated by the score_model_output.sh script"
	exit 0
fi
if [[ ! -f $1 ]]; then
	echo "$1: No such file"; exit
fi

outputs=$1

grep "^HYP:" ${outputs} | cut -d' ' -f2- | awk '{print $0,"(1_"NR")"}' > ${outputs}.sclite.hyp
grep "^REF:" ${outputs} | cut -d' ' -f2- | awk '{print $0,"(1_"NR")"}' > ${outputs}.sclite.ref

sclite -i spu_id -r ${outputs}.sclite.ref -h ${outputs}.sclite.hyp

